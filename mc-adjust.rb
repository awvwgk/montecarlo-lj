		#  | o     * | o     * | o     * | 
		# -|-+-+-+-+-|-+-+-+-+-|-+-+-+-+-|-
		#    |--3--|-2-| mit 3 + 2 = box
		# da 3 > box/2  => box - 3 = 2
		self > $hbox ? $box - self : self
	end
